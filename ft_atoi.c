#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	int				s;
	unsigned long	n;

	n = 0;
	s = 1;
	i = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
	i++;
	if (str[i] == '-' || str[i] == '+')
		if (str[i++] == '-')
			s = -s;
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + str[i++] - 48;
		if (n >= 9223372036854775807)
		{
			if (s == 1)
				return (-1);
			else
				return (0);
		}
	}
	return (s * n);
}
