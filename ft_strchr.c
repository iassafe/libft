#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	j;

	j = 0;
	while (s[j] != (char)c)
	{
		if (s[j] == '\0')
			return (0);
		j++;
	}
	return ((char *)s + j);
}
